public class HelloWorld {
    // Main-Methode: wird am Anfang des Programms aufgerufen
    public static void main(String[] args){
        System.out.println("Hello World");
        System.out.println("Mein Name ist ...");
        System.out.println("Wie alt bin ich?");

        // Variablendeklaration: Datentyp + Variablenname;
        // int: ganze Zahl (kein Komma)
        int zahl1;
        // Variable initialisieren mit =
        zahl1 = 5;
        System.out.println(zahl1);
        System.out.println("zahl1");
        // Variable deklarieren und initialisieren in
        // einem Schritt
        int zahl2 = 10;
        int ergebnis = zahl1 + zahl2;
        System.out.println(ergebnis);
    }


}
